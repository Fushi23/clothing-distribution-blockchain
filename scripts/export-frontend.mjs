// Exports the compiled ABI + the locally-deployed address into the frontend
// as src/contract.js. Run after `hardhat compile` and an Ignition deploy:
//
//   node scripts/export-frontend.mjs
//
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const artifactPath = path.join(
  root,
  "artifacts/contracts/ClothingDistribution.sol/ClothingDistribution.json"
);
const deployedPath = path.join(
  root,
  "ignition/deployments/chain-31337/deployed_addresses.json"
);
const outPath = path.join(root, "frontend-react/src/contract.js");

if (!fs.existsSync(artifactPath)) {
  throw new Error("Artifact not found. Run `npx hardhat compile` first.");
}

const artifact = JSON.parse(fs.readFileSync(artifactPath, "utf8"));

let address = "0x5FbDB2315678afecb367f032d93F642f64180aa3"; // default local
if (fs.existsSync(deployedPath)) {
  const deployed = JSON.parse(fs.readFileSync(deployedPath, "utf8"));
  const key = Object.keys(deployed).find((k) => k.includes("ClothingDistribution"));
  if (key) address = deployed[key];
}

const banner = "// AUTO-GENERATED by scripts/export-frontend.mjs — do not edit by hand.\n";
const file =
  banner +
  `export const CONTRACT_ADDRESS = ${JSON.stringify(address)};\n\n` +
  `export const CONTRACT_ABI = ${JSON.stringify(artifact.abi, null, 2)};\n`;

fs.writeFileSync(outPath, file);
console.log(`Wrote ${outPath}`);
console.log(`  address: ${address}`);
console.log(`  abi entries: ${artifact.abi.length}`);
